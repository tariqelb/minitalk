#include "minitalk_bonus.h"

void	ft_putnbr(size_t n)
{
	if (n < 0)
	{
		write(1, "-", 1);
		n = n * (-1);
	}
	while (n >= 10)
	{
		ft_putnbr(n / 10);
		n = n % 10;
	}
	n = n + 48;
	write(1, &n, 1);
}
